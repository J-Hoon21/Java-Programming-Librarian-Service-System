    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        id = ID.getText();
        name = Name.getText();
        genre = Genre.getSelectedItem().toString();
        author = Author.getText();
        publisher = Publisher.getText();
        year = Year.getText();
        summary = Summary.getText();
        if (id.equals("") || name.equals("") || genre.equals("") || author.equals("") || publisher.equals("") || year.equals("") || summary.equals(""))
        {
         JOptionPane.showMessageDialog(null, "Please Fill In All Valid Data!!");   
        }
       else {result = JOptionPane.showConfirmDialog(this, "Are You Sure You Want To Save The Data?", "Save To Library", JOptionPane.ERROR_MESSAGE);
       if (result == JOptionPane.YES_OPTION)
            //setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
           try {
                FileWriter fw = new FileWriter("books.txt", true); // insert True is to insert new record instead of overwrite
                 PrintWriter out = new PrintWriter(fw);
                      out.write(id + ":" + name + ":" + genre + ":" + author + ":" + publisher + ":" + year + ":" +summary);
                      out.write(System.getProperty("line.separator"));
                      out.close();
                      fw.close();
                      ID.setText("");
                      Name.setText("");
                      Author.setText("");
                      Publisher.setText("");
                      Year.setText("");
                      Summary.setText("");
            } catch (Exception ae){
                JOptionPane.showMessageDialog(null, ae);
            }
        }
    }//GEN-LAST:event_SaveActionPerformed

