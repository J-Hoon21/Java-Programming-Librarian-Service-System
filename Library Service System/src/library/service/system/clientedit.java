    private void VerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerifyActionPerformed
        // TODO add your handling code here:
        id = txtID.getText();
        name = txtName.getText();
        String status = "fail";
        try {
            File file = new File("client.txt");
            Scanner inputFile = new Scanner(file); 
            while (inputFile.hasNext()) {       // Read lines from the file until no more are left.
                String line = inputFile.nextLine();
                String[] details = line.split(":");
                String ID = details[0];
                String Name = details[1];
                String Gender = details[2];
                String Role = details[3];
                String Uni = details[4];
                String Personal = details[5];
                    if (id.equals(ID))
                    {
                    txtID.setText(ID);
                    txtName.setText(Name);
                    cbxGender.setSelectedItem(Gender);
                    CbxRole.setSelectedItem(Role);
                    txtUni.setText(Uni);
                    txtPersonal.setText(Personal);
                    status = "success";
                    }}
                    if (status=="fail")
                    {
                    JOptionPane.showMessageDialog(this, "Oops! No Results! Please Input Available Client ID!");
                    }
            }catch (Exception ae){
            JOptionPane.showMessageDialog(null, ae);
      }
                    
    }//GEN-LAST:event_VerifyActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        id = txtID.getText();
        name = txtName.getText();
        gender = cbxGender.getSelectedItem().toString();
        role = CbxRole.getSelectedItem().toString();
        uni = txtUni.getText();
        personal = txtPersonal.getText();
        String oldContent = "";
        String status = "fail";
        try
        {   
            File file = new File("client.txt");
            Scanner inputFile = new Scanner(file); 
            
            while (inputFile.hasNext()){ // Read lines from the file until no more are left.
            String line = inputFile.nextLine();
            String[] details = line.split(":");
            
                if (id.equals(details[0])){
                Scanner sc = new Scanner(new File ("client.txt")); 
                    while (sc.hasNextLine()) 
                    {
                     oldContent = oldContent + sc.nextLine() + System.lineSeparator();//Reading all the lines of input text file into oldContent
                    }       
                String oldString = details[0] + ":" + details[1] + ":" + details[2] + ":" + details[3] + ":" + details[4] + ":" + details[5];
                String newString = details[0] + ":" + name + ":" + gender + ":" + role + ":" + uni + ":" + personal;
                //Replacing oldString with newString in the oldContent
                String newContent = oldContent.replaceAll(oldString, newString);
                //Rewriting the input text file with newContent
                FileWriter writer = new FileWriter("client.txt");
                writer.append(newContent);
                writer.flush();
                writer.close();
                status = "success";
                JOptionPane.showMessageDialog(this, "Client Edit Successful!");
                txtID.setText("");
                txtName.setText("");
                txtUni.setText("");
                txtPersonal.setText("");
                }
             }
                if (status=="fail"){
                    JOptionPane.showMessageDialog(this, "Oops! No Results!");
                    }} catch (Exception ae){
                    JOptionPane.showMessageDialog(null, ae);
                    }
    }//GEN-LAST:event_SaveActionPerformed

